
// take two variable a and b and print their sum

import java.util.Scanner;

public class input {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);    // for taking input from user
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;
        System.out.println(sum);         // printing output
    }
}
